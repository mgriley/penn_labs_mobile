    override func viewWillAppear(_ animated: Bool) {
        
        // load the history from the plist
        let url = FileManager.default.urls(for: .documentDirectory, in: .allDomainsMask).first!
        let fileUrl = url.appendingPathComponent("Data.plist")
        let arr = NSArray(contentsOfFile: fileUrl.path)
        pastSearches = arr as! [String]
        table.reloadData()
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return pastSearches.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: cellId, for: indexPath) as! HistoryCell
        let index = indexPath.row
        cell.setText(text: pastSearches[index])
        return cell
    }

